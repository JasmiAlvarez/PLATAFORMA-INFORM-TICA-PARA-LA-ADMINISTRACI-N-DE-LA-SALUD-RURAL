    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

      
         try {
      String fecha = txtfecha.getText().trim();
        String hora = txthora.getText().trim();
           String dni = txtdni.getText().trim();
      int codigopaciente =  Integer.parseInt(txtcodigopaciente.getText());
      
         int codigomedico=  Integer.parseInt(txtcodigomedico.getText());
   
        String estado = txtestado.getText().trim();

  
        if (dni.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe consultar al  paciente por DNI.", "Falta nombre", JOptionPane.WARNING_MESSAGE);
            txtdni.requestFocus();
            return;
        }

       

      

        CitaDAO dao = new CitaDAO();
      

       

        if (fecha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese la fecha.", "Falta fecha", JOptionPane.WARNING_MESSAGE);
            txtfecha.requestFocus();
            return;
        }

      


if (fecha.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Ingrese la fecha.", "Falta fecha", JOptionPane.WARNING_MESSAGE);
    txtfecha.requestFocus();
    return;
}


if (!fecha.matches("\\d{4}-\\d{2}-\\d{2}")) {
    JOptionPane.showMessageDialog(this, "Ingrese la fecha en formato correcto (AAAA-MM-DD).", "Formato incorrecto", JOptionPane.WARNING_MESSAGE);
    txtfecha.requestFocus();
    return;
}
        if (hora.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese la hora.", "Falta motivo", JOptionPane.WARNING_MESSAGE);
            txthora.requestFocus();
            return;
        }

 


if (!hora.matches("([01]\\d|2[0-3]):[0-5]\\d")) {
    JOptionPane.showMessageDialog(this, "Ingrese la hora en formato correcto (HH:mm).", "Formato incorrecto", JOptionPane.WARNING_MESSAGE);
    txthora.requestFocus();
    return;
}

       Cita nuevo = new Cita();
        nuevo.setFecha(fecha);
        nuevo.setHora(hora);
        
        nuevo.setCodigopaciente(codigopaciente);
        
        
         Especialidad esp = (Especialidad) cboespecialidad.getSelectedItem();
        int idespecialidad = esp.getIdespecialidad();

          nuevo.setIdespecialidad(idespecialidad); 

          nuevo.setCodigomedico(codigomedico);
          
        nuevo.setMotivo(estado);

        dao.registrar(nuevo);

        JOptionPane.showMessageDialog(this, "Agenda Cita registrado exitosamente.", "Registro correcto", JOptionPane.INFORMATION_MESSAGE);
        limpiar();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al registrar agenda cita: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }

        
        
        
        
        
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed

        
         String dni = txtdni.getText().trim();

    if (dni.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el DNI del paciente.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        txtdni.requestFocus();
        return;
    }

    CitaDAO dao = new CitaDAO(); 
    Paciente paciente = dao.buscarPacientePorDni(dni);

    if (paciente != null) {
        txtcodigopaciente.setText(String.valueOf(paciente.getCodigopaciente()));
        txtnombres.setText(paciente.getNombres());
        txtapellidos.setText(paciente.getApellidos());
        txtmotivo.setText(paciente.getMotivo());
    
    } else {
        JOptionPane.showMessageDialog(this, "Paciente no encontrado.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        limpiar();
    }

        
        
        
        
        
        
    }//GEN-LAST:event_btnbuscarActionPerformed

