    private void btnGCBuscarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGCBuscarPacienteActionPerformed
        // TODO add your handling code here:
        int row = 0;
        String busqueda = this.txtGCBuscarPaciente.getText();
        cConnection con = new cConnection();
        
        String query;
        if (busqueda.matches("-?\\d+")) { // Verifica si 'nombre' es numérico
            query = "SELECT * FROM PACIENTE WHERE codigo = " + busqueda;
        } else {
            query = "SELECT * FROM PACIENTE WHERE nombre like '%" + busqueda + "%'";
        }
        
        try
        {
          DefaultTableModel model = (DefaultTableModel) tblGCPaciente.getModel();
          Statement st = con.ObtenerConexion().createStatement();
          ResultSet rs = st.executeQuery(query);
          model.setRowCount(0);
          while (rs.next()) {
                int cod = Integer.parseInt(rs.getString("codigo"));
                String nomb = rs.getString("nombre");

                // Agregar cada fila al modelo de la tabla
                model.insertRow(row, new Object[]{cod, nomb});
                row++;
            }
        }
        catch(Exception e)
        {
          JOptionPane.showMessageDialog(null,"No existe el Usuario","Mensaje",JOptionPane.CANCEL_OPTION);
        }
    }//GEN-LAST:event_btnGCBuscarPacienteActionPerformed

